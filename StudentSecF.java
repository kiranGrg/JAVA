    private String[] getId(String sname, String sgrade, String sroll) {
        //String[] studentid = null;
        Student s = new Student();
        String stname = "";
        String stu_id = "";

        if (sname.length() > 1 && sgrade != null && sroll != null) {
            stname = getName(sname).trim();
            if (stname.length() > 1) {
                //s.Connect();
                stu_id = s.getValidation(stname, sgrade, Integer.parseInt(sroll));
                s.Close();
            } else {
                JOptionPane.showMessageDialog(this, "Enter the full name !!!!", "Notice", JOptionPane.PLAIN_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill up the both field", "Notice", JOptionPane.PLAIN_MESSAGE);
        }

        return new String[]{stu_id, stname};

    }

    private String getName(String name) {
        int len1 = 0, len2 = 0, i = 0, j = 0;
        String sname = "", s1 = "", s2 = "", s3 = "", s4 = "";


        i = name.indexOf(" ");
        if (i > 0) {
            len1 = name.length();
            s1 = name.substring(0, i);
            sname += s1 + " ";
            s2 = (name.substring(i, len1)).trim();

            j = s2.indexOf(" ");
            if (j > 0) {
                len2 = s2.length();
                s3 = s2.substring(0, j);
                sname += s3 + " ";
                s4 = (s2.substring(j, len2)).trim();
                sname += s4 + " ";
            } else {
                sname += s2;
            }
        }

        return (sname);
    }

