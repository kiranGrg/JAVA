    private void infoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_infoActionPerformed
        // TODO add your handling code here:
        Student s = new Student();
        String sname = (name.getText()).trim();
        String sgrade = (grade.getText()).trim();
        String sroll = (roll.getText()).trim();
        int iroll = Integer.parseInt(sroll);



        if (sname.length() > 1 && sgrade.length() > 1 && sroll.length() >= 1) {
            String stname = getName(sname);
            if (stname.length() > 1) {
                String query = "SELECT * FROM student_info WHERE name = '"+stname+"' AND grade = '"+sgrade+"' AND roll = '"+iroll+"'";
                //s.Connect();
                ResultSet rs = s.getInfo(query);
                if (rs != null) {
                    new StudentInfoF(rs).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Data not Found");
                    name.setText("");
                    grade.setText("");
                    roll.setText("");
                    s.Close();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Enter the full name !!!!", "Notice", JOptionPane.PLAIN_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill up the both field", "Notice", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_infoActionPerformed

