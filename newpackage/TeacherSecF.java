    private void infoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_infoActionPerformed
        // TODO add your handling code here:
        String sname = (txtname.getText()).trim();
        String teacherId = (txtid.getText()).trim();

        if (!sname.equalsIgnoreCase("") && !teacherId.equalsIgnoreCase("")) {
            String stname = getName(sname);
            if (!stname.equals("")) {
                query = "SELECT * FROM teacher_info WHERE name = '"+stname+"'"
                                + "AND teacherId = '"+teacherId+"'";
                Teacher t = new Teacher();
                ResultSet rs = t.getTeacherInfo(query);
                if(rs != null){
                    new TeacherInfoF(rs).setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this,"Data Not Found or Error.");
                    txtname.setText("");
                    txtid.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Enter the full name !!!!", "Notice", JOptionPane.PLAIN_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill up the both field", "Notice", JOptionPane.PLAIN_MESSAGE);
        }
}//GEN-LAST:event_infoActionPerformed

